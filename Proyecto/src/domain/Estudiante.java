public class Estudiante extends Persona {

    private int id;
    private String carnet;

    public Estudiante() {
        this.id = 0;
        this.carnet = "";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

}
